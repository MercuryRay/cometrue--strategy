/** カテゴリごとにグルーピング（UI表示用ヘルパー） */
export const FAQ_BY_CATEGORY: Record<FAQCategory, FAQItem[]> = FAQ_ITEMS.reduce(
  (acc, item) => {
    (acc[item.category] ||= []).push(item);
    return acc;
  },
  {} as Record<FAQCategory, FAQItem[]>
);

/** JSON-LD FAQPage 生成（SEO構造化データ） */
export function buildFaqJsonLd(items: readonly FAQItem[] = FAQ_ITEMS): string {
  const data = {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: items.map((it) => ({
      '@type': 'Question',
      name: it.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: it.answer,
      },
    })),
  };
  return JSON.stringify(data);
}
